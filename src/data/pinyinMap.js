// 冻结防止 Vue 响应式代理
export const PINYIN_MAP = Object.freeze(pinyinMap)

/**
 * 根据拼音 buffer 获取候选汉字
 * @param {string} buf - 拼音输入缓冲区
 * @returns {{ exact: string[], prefix: string[] }}
 */
export function getPinyinCandidates(buf) {
  if (!buf) return { exact: [], prefix: [] }
  const b = buf.toLowerCase()
  const exact = PINYIN_MAP[b] ? [...PINYIN_MAP[b]] : []
  const prefix = []
  for (const [py, ch] of Object.entries(PINYIN_MAP)) {
    if (py.startsWith(b) && py !== b) {
      for (const c of ch) {
        prefix.push(c)
      }
    }
  }
  return { exact, prefix }
}
